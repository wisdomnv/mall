<template>
  <div class="tab-control">
    <div
      v-for="(item, index) in titles"
      class="tab-control-item"
      :class="{ active: index === tabindex }"
      @click="itemclick(index)"
    >
      <span>{{ item }}</span>
    </div>
  </div>
</template>

<script>
export default {
  name: "TabControl",
  props: {
    titles: {
      type: Array,
      default() {
        return [];
      },
    },
  },
  data() {
    return {
      tabindex: 0,
    };
  },
  methods: {
    itemclick(index) {
      this.tabindex = index;
      this.$emit("tabclick", index);
    },
  },
};
</script>

<style scoped>
